;--------------------------------
; Программа вычисления выражения
;--------------------------------
%include 'in_out.asm'          ; подключение внешнего файла in_out.asm

SECTION .data
div: DB 'Результат: ', 0        ; Сообщение для результата
rem: DB 'Остаток от деления: ', 0 ; Сообщение для остатка

SECTION .text
GLOBAL _start

_start:
    ; ---- Вычисление выражения f(x) = (4 * 6 + 2) / 5 ----
    mov eax, 4                 ; EAX = 4
    mov ebx, 6                 ; EBX = 6
    mul ebx                    ; EAX = EAX * EBX (4 * 6 = 24)
    add eax, 2                 ; EAX = EAX + 2 (24 + 2 = 26)
    xor edx, edx               ; Обнуляем EDX для корректного деления
    mov ebx, 5                 ; EBX = 5
    div ebx                    ; EAX = EAX / 5, EDX = остаток (EAX = 5, EDX = 1)

    mov edi, eax               ; Сохранение результата (целого числа) в EDI

    ; ---- Вывод результата на экран ----
    mov eax, div               ; Загрузить сообщение 'Результат: '
    call sprint                ; Печать сообщения
    mov eax, edi               ; Загрузить результат
    call iprintLF              ; Печать результата с переводом строки

    mov eax, rem               ; Загрузить сообщение 'Остаток от деления: '
    call sprint                ; Печать сообщения
    mov eax, edx               ; Загрузить остаток
    call iprintLF              ; Печать остатка с переводом строки

    call quit                  ; Выход из программы

